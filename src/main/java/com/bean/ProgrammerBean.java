package com.bean;

public class ProgrammerBean {
	private int pId;
	private String pName;
	private String pDomain;
	private int pSalary;
	
	
	
	public int getpId() {
		return pId;
	}
	public void setpId(int pId) {
		this.pId = pId;
	}
	public String getpName() {
		return pName;
	}
	public void setpName(String pName) {
		this.pName = pName;
	}
	public String getpDomain() {
		return pDomain;
	}
	public void setpDomain(String pDomain) {
		this.pDomain = pDomain;
	}
	public int getpSalary() {
		return pSalary;
	}
	public void setpSalary(int pSalary) {
		this.pSalary = pSalary;
	}
}
